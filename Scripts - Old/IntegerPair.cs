public struct IntegerPair
{
    private int intX;
    private int intY;

    public int x
    {
        get
        {
            return intX;
        }
        set
        {
            intX = value;
        }
    }
    public int y
    {
        get
        {
            return intY;
        }
        set
        {
            intY = value;
        }
    }
    public int i
    {
        get
        {
            return intX;
        }
        set
        {
            intX = value;
        }
    }
    public int j
    {
        get
        {
            return intY;
        }
        set
        {
            intY = value;
        }
    }


    public IntegerPair(int x, int y)
    {
        this.intX = x;
        this.intY = y;
    }

}
